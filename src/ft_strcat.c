#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*s1_copy;

	s1_copy = s1 + ft_strlen(s1);
	while (*s2)
		*s1_copy++ = *s2++;
	*s1_copy = '\0';
	return (s1);
}
