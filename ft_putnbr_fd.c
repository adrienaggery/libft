#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{

	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		ft_putnbr(-nb);
	}
	else
		ft_putchar_fd(nb + '0', fd);
}
